void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
  uint32_t now = HAL_GetTick();
  if (GPIO_Pin == GPIO_PIN_0 && (now - lastPress > 200))
  {
    lastPress = now;
    HAL_GPIO_TogglePin(LD3_GPIO_Port, LD3_Pin);
  }
}


